CREATE TABLE IF NOT EXISTS `tb_tarot_card` (
    `id` INT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '主键ID',
    `card_id` TINYINT UNSIGNED NOT NULL COMMENT '牌ID（1-78）',
    `card_name_cn` VARCHAR(50) NOT NULL COMMENT '中文牌名',
    `card_name_en` VARCHAR(100) DEFAULT NULL COMMENT '英文牌名',
    `card_type` ENUM('MAJOR_ARCANA','MINOR_ARCANA') NOT NULL COMMENT '牌类型（大阿卡纳/小阿卡纳）',
    `suit` ENUM('NONE','WANDS','CUPS','SWORDS','PENTACLES') DEFAULT NULL COMMENT '花色（无/权杖/圣杯/宝剑/星币）',
    `number` TINYINT UNSIGNED DEFAULT NULL COMMENT '数字（1-14，大牌为0）',
    `symbol` VARCHAR(10) DEFAULT '🎴' COMMENT '符号/图标',
    `keyword_up` VARCHAR(200) DEFAULT NULL COMMENT '正位关键词',
    `keyword_rev` VARCHAR(200) DEFAULT NULL COMMENT '逆位关键词',
    `meaning_up` TEXT COMMENT '正位含义',
    `meaning_rev` TEXT COMMENT '逆位含义',
    `description` TEXT COMMENT '牌面描述',
    `interpretation_up` TEXT COMMENT '正位详细解读',
    `interpretation_rev` TEXT COMMENT '逆位详细解读',
    `love_up` TEXT COMMENT '正位爱情含义',
    `love_rev` TEXT COMMENT '逆位爱情含义',
    `career_up` TEXT COMMENT '正位事业含义',
    `career_rev` TEXT COMMENT '逆位事业含义',
    `wealth_up` TEXT COMMENT '正位财运含义',
    `wealth_rev` TEXT COMMENT '逆位财运含义',
    `health_up` TEXT COMMENT '正位健康含义',
    `health_rev` TEXT COMMENT '逆位健康含义',
    `advice_up` TEXT COMMENT '正位建议',
    `advice_rev` TEXT COMMENT '逆位建议',
    `image_url` VARCHAR(500) DEFAULT NULL COMMENT '牌面图片URL',
    `sort_order` TINYINT UNSIGNED NOT NULL DEFAULT 0 COMMENT '排序顺序',
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (`id`),
    UNIQUE KEY `uk_card_id` (`card_id`),
    KEY `idx_card_type` (`card_type`),
    KEY `idx_suit` (`suit`),
    KEY `idx_sort_order` (`sort_order`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='塔罗牌详细信息表';

